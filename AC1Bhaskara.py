#programação estruturada AC1

#ax**2 + bx + c = 0

a = float(input("Digite um número para a:"))
b = float(input("Digite um número para b:"))
c = float(input("Digite um número para c:"))
delta = (b ** 2) - 4 * a * c

float(delta)

equacao1 = (-b - (delta ** 0.5)) / (2 * a)
equacao2 = (-b + (delta ** 0.5)) / (2 * a)

print(equacao1)
print(equacao2)



















